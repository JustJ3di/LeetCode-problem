
class Solution {
public:
    int romanToInt(string s) {
        int n = 0;
        for(int i = 0;i <= s.size();i++){

            if (s[i] == 'I') {
                n += 1;
            }
            if (s[i] == 'V') {
                n += 5;
                if (i != 0 && s[i - 1] == 'I') {
                    n -= 2;
                }
            }
            if (s[i] == 'X') {
                if (i != 0 && s[i - 1] == 'I') {
                    n -= 2;
                }
                if (i != 0 && s[i - 1] == 'V') {
                    n -= 10;
                }
                n += 10;
            }
            if (s[i] == 'L') {
                if (i != 0 && s[i - 1] == 'I') {
                    n -= 2;
                }
                if (i != 0 && s[i - 1] == 'V') {
                    n -= 10;
                }
                if (i != 0 && s[i - 1] == 'X') {
                    n -= 20;
                }
                n += 50;
            }
            if (s[i] == 'C') {
                n += 100;
                if (i != 0 && s[i - 1] == 'I') {
                    n -= 2;
                }
                if (i != 0 && s[i - 1] == 'V') {
                    n -= 10;
                }
                if (i != 0 && s[i - 1] == 'X') {
                    n -= 20;
                }
                if (i != 0 && s[i - 1] == 'L') {
                    n -= 100;
                }
            }
            if (s[i] == 'D') {
                n += 500;
                if (i != 0 && s[i - 1] == 'I') {
                    n -= 2;
                }
                if (i != 0 && s[i - 1] == 'V') {
                    n -= 10;
                }
                if (i != 0 && s[i - 1] == 'X') {
                    n -= 20;
                }
                if (i != 0 && s[i - 1] == 'L') {
                    n -= 100;
                }
                if (i != 0 && s[i - 1] == 'C') {
                    n -= 200;
                }
            }
            if (s[i] == 'M') {
                n += 1000;
                if (i != 0 && s[i - 1] == 'I') {
                    n -= 2;
                }
                if (i != 0 && s[i - 1] == 'V') {
                    n -= 10;
                }
                if (i != 0 && s[i - 1] == 'X') {
                    n -= 20;
                }
                if (i != 0 && s[i - 1] == 'L') {
                    n -= 100;
                }
                if (i != 0 && s[i - 1] == 'C') {
                    n -= 200;
                }
                if (i != 0 && s[i - 1] == 'D') {
                    n -= 1000;
                }
              
            }
            
        }
        return n;
    }
};
